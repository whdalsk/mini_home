package net.hb.miniPhoto;

public class MiniPhotoReplyDTO {
	int pr_num;
	String pr_writer;
	String pr_content;
	int pr_code;
	
	public int getPr_num() {
		return pr_num;
	}
	public void setPr_num(int pr_num) {
		this.pr_num = pr_num;
	}
	public String getPr_writer() {
		return pr_writer;
	}
	public void setPr_writer(String pr_writer) {
		this.pr_writer = pr_writer;
	}
	public String getPr_content() {
		return pr_content;
	}
	public void setPr_content(String pr_content) {
		this.pr_content = pr_content;
	}
	public int getPr_code() {
		return pr_code;
	}
	public void setPr_code(int pr_code) {
		this.pr_code = pr_code;
	}
	
}// class MiniPhotoReplyDTO END
