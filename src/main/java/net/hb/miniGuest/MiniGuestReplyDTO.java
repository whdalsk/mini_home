package net.hb.miniGuest;

public class MiniGuestReplyDTO {
	int gr_num;
	String gr_writer;
	String gr_content;
	int gr_code;
	
	public int getGr_num() {
		return gr_num;
	}
	public void setGr_num(int gr_num) {
		this.gr_num = gr_num;
	}
	public String getGr_writer() {
		return gr_writer;
	}
	public void setGr_writer(String gr_writer) {
		this.gr_writer = gr_writer;
	}
	public String getGr_content() {
		return gr_content;
	}
	public void setGr_content(String gr_content) {
		this.gr_content = gr_content;
	}
	public int getGr_code() {
		return gr_code;
	}
	public void setGr_code(int gr_code) {
		this.gr_code = gr_code;
	}
	
}// class MiniGuestReplyDTO END
